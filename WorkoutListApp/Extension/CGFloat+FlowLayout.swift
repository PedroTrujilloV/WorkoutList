import Foundation
import UIKit

public extension CGFloat {
    /// WorkoutListApp enum that defines dimensions for the collection view flow layout
    enum FlowLayout {
        enum Spacing {
            public static let small:CGFloat = 1.0
            public static let normal:CGFloat = 2.0
            public static let big:CGFloat = 3.0
        }
    }
}
